/**
 * 人格库提取 Prompt
 * 从小说正文中提取作者的"人味指纹"
 * 每个维度要求具体、可计量的材料池，不做概括摘要
 */

export { sampleText } from './disassembler'

export const PERSONALITY_EXTRACTION_SYSTEM = `从作者的文本中提取以下9个维度的人味指纹。每个维度的回答必须具体、有细节、可引用原文——不是概括，是材料池。

### 1. 私人意象
找出至少5个作者反复使用的、不属于通用套路词典的具体意象。
对每个意象：描述它是什么、出现在什么情境中、和哪个角色或状态关联。
不要写"某人喜欢用XX意象"这种概括句——直接列出意象。

### 2. 情绪怪癖
找出至少3种情绪在角色身上的反常表达方式。
不是"手在抖""咬牙"这种通用反应——是这个作者特有的。
对每种情绪：描述角色在最极端时会做什么非标准动作。如果可以，注明这个动作在哪个场景出现过。

### 3. 节奏指纹
描述作者在时间线上的呼吸方式：
- 什么时候突然加速（短段连发）？
- 什么时候沉默或走神（插入无关细节）？
- 每章通常有几个"断点"（读者需要深呼吸的地方）？
- 高潮前后的节奏是否对称？

### 4. 废话风格
找出叙述者"插嘴"或角色"跑题"的具体表现：
- 叙述者会突然用什么口吻说话？（如：突然用第一人称点评、突然反问读者）
- 角色对话中跑题的类型是什么？（如：吵架吵到一半突然讲起食物的做法）
- 有没有被反复使用的、看似无意义的句式或口头禅？
- 无关细节的偏好是什么？（如：总是描写食物、总是写鞋子的状态）

### 5. 私人修辞
找出至少3个从作者生活经验里长出来的比喻/修辞。
对每个修辞：描述比喻的本体和喻体，以及为什么这个喻体不是从文学词典里来的——它"扎根"在什么具体的生活经验上？

### 6. 对话指纹
分析作者写人物对话的独特方式：
- 情绪在对话中如何体现？（语气词、打断、沉默、重复、改口——具体是哪种）
- 角色说话时"说了多少、藏了多少"？（是直接表达还是绕着说？潜台词多还是少？）
- 不同角色的说话方式有没有明显区别？（用词、句长、节奏）
- 对话和叙述的比例大概是多少？
- 举2-3句最能代表这个作者对话风格的原文。

### 7. 风景/环境描写指纹
分析作者如何处理景物和环境：
- 风景描写的频率：每章大概几处？密集还是稀疏？
- 描写方式：是全景铺陈还是一两个细节切入？是静态描写还是在角色行动中带出？
- 风景如何承载情绪？（如：紧张时写什么、悲伤时写什么、平静时写什么）
- 有没有反复出现的环境元素？（如总是写天气、总是写光线、总是写声音）
- 举1-2处最典型的风景描写。

### 8. 叙事距离指纹
分析叙述者与角色之间的距离：
- 叙述者通常离角色的内心有多近？（自由间接引语、心理描写频率、还是严格外部视角）
- 叙述者会不会直接点评角色的行为或想法？用什么口吻？
- 视角是否在不同角色间切换？切换频率？
- 读者知道的是"角色在想什么"还是"角色做了什么"？

### 9. 信息释放指纹
分析作者如何向读者释放信息：
- 关键信息是通过对话、叙述、还是动作揭示的？
- 信息是一次性给还是分批释出？有没有明显的"藏—露"节奏？
- 有没有反复使用的伏笔回收模式？

## 输出
JSON，不要markdown代码块：
{"private_imagery":"200-300字","emotional_quirks":"150-200字","rhythm_fingerprint":"150-200字","nonsense_style":"150-200字","private_rhetoric":"150-200字","dialogue_fingerprint":"150-200字","scenery_fingerprint":"150-200字","narrative_distance":"100-150字","info_release":"100-150字","raw_analysis":""}
如果某个维度信息不足，写"从采样中无法确定"。`

export const PERSONALITY_EXTRACTION_USER = (text: string) => `从以下文本中提取9维人味指纹。每个维度请给出具体材料，不要概括。

【源文本】
${text}

输出 JSON。`
