export { sampleText } from './disassembler'

export const PERSONALITY_EXTRACTION_SYSTEM = `从作者的文本中提取以下5个维度的人味指纹。每个维度的回答必须具体、有细节、可引用原文——不是概括，是材料池。

### 1. 私人意象
找出至少5个作者反复使用的、不属于通用套路词典的具体意象。
对每个意象：描述它是什么、出现在什么情境中、和哪个角色或状态关联。
不要写"某人喜欢用XX意象"这种概括句——直接列出意象。

### 2. 情绪怪癖
找出至少3种情绪在角色身上的反常表达方式。
不是"手在抖""咬牙"这种通用反应——是这个作者特有的。
对每种情绪：描述角色在最极端时会做什么非标准动作。如果可以，注明这个动作在哪个场景出现过。

### 3. 节奏指纹
描述作者在时间线上的呼吸方式：
- 什么时候突然加速（短段连发）？
- 什么时候沉默或走神（插入无关细节）？
- 每章通常有几个"断点"（读者需要深呼吸的地方）？
- 高潮前后的节奏是否对称？

### 4. 废话风格
找出叙述者"插嘴"或角色"跑题"的具体表现：
- 叙述者会突然用什么口吻说话？（如：突然用第一人称点评、突然反问读者）
- 角色对话中跑题的类型是什么？（如：吵架吵到一半突然讲起食物的做法）
- 有没有被反复使用的、看似无意义的句式或口头禅？
- 无关细节的偏好是什么？（如：总是描写食物、总是写鞋子的状态）

### 5. 私人修辞
找出至少3个从作者生活经验里长出来的比喻/修辞。
对每个修辞：描述比喻的本体和喻体，以及为什么这个喻体不是从文学词典里来的——它"扎根"在什么具体的生活经验上？

## 输出
JSON，不要markdown代码块：
{"private_imagery":"列出至少5个意象及关联，200-300字","emotional_quirks":"至少3种情绪×反常反应，150-200字","rhythm_fingerprint":"呼吸方式+断点+对称性，150-200字","nonsense_style":"插嘴/跑题/口头禅/无关细节，150-200字","private_rhetoric":"至少3个比喻，标注生活来源，150-200字","raw_analysis":""}
如果某个维度信息不足，写"从采样中无法确定"。`

